package de.pfeiffy.excel;

public class Mapping {

	String Blatt;
	String Bezeichnung;
	String Zellenkoordinate;
	String Format;
	String Ziel;
	String Profil;

	public String getBlatt() {
		return Blatt;
	}

	public void setBlatt(String blatt) {
		Blatt = blatt;
	}

	public String getBezeichnung() {
		return Bezeichnung;
	}

	public void setBezeichnung(String bezeichnung) {
		Bezeichnung = bezeichnung;
	}

	public String getZellenkoordinate() {
		return Zellenkoordinate;
	}

	public void setZellenkoordinate(String zellenkoordinate) {
		Zellenkoordinate = zellenkoordinate;
	}

	public String getFormat() {
		return Format;
	}

	public void setFormat(String format) {
		Format = format;
	}

	public String getZiel() {
		return Ziel;
	}

	public void setZiel(String ziel) {
		Ziel = ziel;
	}

	public String getProfil() {
		return Profil;
	}

	public void setProfil(String profil) {
		Profil = profil;
	}

}
